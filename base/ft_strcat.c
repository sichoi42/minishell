#include "../minishell.h"

char	*ft_strcat(char *org, char *target)
{
	int	i;

	i = -1;
	while (target[++i] != '\0')
		;
	while (*org != '\0')
		target[i++] = *org++;
	target[i] = '\0';
	return (target);
}
